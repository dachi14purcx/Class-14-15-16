// os_path_example.js

// ­ЪЊд OS рЃЏрЃЮрЃЊрЃБрЃџрЃў
const os = require("os");
const path = require("path");

console.log("=== OS рЃЏрЃЮрЃЊрЃБрЃџрЃў ===");

// 1) os.type() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃЮрЃърЃћрЃарЃљрЃфрЃўрЃБрЃџрЃў рЃАрЃўрЃАрЃбрЃћрЃЏрЃўрЃА рЃАрЃљрЃ«рЃћрЃЮрЃЉрЃљрЃА
console.log("OS рЃбрЃўрЃърЃў:", os.type());

// 2) os.arch() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃАрЃўрЃАрЃбрЃћрЃЏрЃўрЃА рЃљрЃарЃЦрЃўрЃбрЃћрЃЦрЃбрЃБрЃарЃљрЃА (x64 рЃљрЃю ia32)
console.log("рЃљрЃарЃЦрЃўрЃбрЃћрЃЦрЃбрЃБрЃарЃљ:", os.arch());

// 3) os.platform() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃърЃџрЃљрЃбрЃцрЃЮрЃарЃЏрЃљрЃА (win32, linux, darwin)
console.log("рЃърЃџрЃљрЃбрЃцрЃЮрЃарЃЏрЃљ:", os.platform());

// 4) os.hostname() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃЎрЃЮрЃЏрЃърЃўрЃБрЃбрЃћрЃарЃўрЃА/рЃАрЃћрЃарЃЋрЃћрЃарЃўрЃА рЃАрЃљрЃ«рЃћрЃџрЃА
console.log("рЃ░рЃЮрЃАрЃбрЃўрЃА рЃАрЃљрЃ«рЃћрЃџрЃў:", os.hostname());

// 5) os.totalmem() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃАрЃўрЃАрЃбрЃћрЃЏрЃўрЃА рЃАрЃљрЃћрЃарЃЌрЃЮ рЃЏрЃћрЃ«рЃАрЃўрЃћрЃарЃћрЃЉрЃљрЃА рЃЉрЃљрЃўрЃбрЃћрЃЉрЃерЃў
console.log("рЃАрЃарЃБрЃџрЃў рЃЏрЃћрЃ«рЃАрЃўрЃћрЃарЃћрЃЉрЃљ (рЃЉрЃљрЃўрЃбрЃћрЃЉрЃерЃў):", os.totalmem());
console.log("рЃАрЃарЃБрЃџрЃў рЃЏрЃћрЃ«рЃАрЃўрЃћрЃарЃћрЃЉрЃљ (рЃњрЃўрЃњрЃљрЃЉрЃљрЃўрЃбрЃћрЃЉрЃерЃў):", (os.totalmem() / 1024 / 1024 / 1024).toFixed(2), "GB");


console.log("\n=== PATH рЃЏрЃЮрЃЊрЃБрЃџрЃў ===");

// 6) path.basename() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃцрЃљрЃўрЃџрЃўрЃА рЃАрЃљрЃ«рЃћрЃџрЃА
const filePath = "/home/user/docs/app.js";
console.log("рЃцрЃљрЃўрЃџрЃўрЃА рЃАрЃљрЃ«рЃћрЃџрЃў:", path.basename(filePath)); // Рєњ app.js

// 7) path.extname() Рєњ рЃљрЃЉрЃарЃБрЃюрЃћрЃЉрЃА рЃцрЃљрЃўрЃџрЃўрЃА рЃњрЃљрЃцрЃљрЃарЃЌрЃЮрЃћрЃЉрЃљрЃА (.js)
console.log("рЃцрЃљрЃўрЃџрЃўрЃА рЃњрЃљрЃцрЃљрЃарЃЌрЃЮрЃћрЃЉрЃљ:", path.extname(filePath)); // Рєњ .js

// 8) path.join() Рєњ рЃљрЃћрЃарЃЌрЃћрЃЉрЃА рЃЉрЃўрЃџрЃўрЃЎрЃћрЃЉрЃА рЃћрЃарЃЌ рЃЏрЃЌрЃџрЃўрЃљрЃю рЃЉрЃўрЃџрЃўрЃЎрЃљрЃЊ
const joinedPath = path.join("user", "docs", "file.txt");
console.log("рЃерЃћрЃћрЃарЃЌрЃћрЃЉрЃБрЃџрЃў рЃЉрЃўрЃџрЃўрЃЎрЃў:", joinedPath);